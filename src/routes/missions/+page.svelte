<h1 class="text-2xl">Mission and Vision</h1>

<img src="/images/1000013259.jpg" alt="">

<p>
H.U.G is a non-profit volunteer organization founded in Orange County in 2014.  It is known as an impactful student-run community service organization led by high-achieving high school students from across Southern California. HUG pursues the mission of making the world a better place and organizes and carries out numerous events and activities throughout the year including cleaning parks and beaches, hosting garage sales and other fundraising for students in need, and working to improve lives internationally. 
</p>

